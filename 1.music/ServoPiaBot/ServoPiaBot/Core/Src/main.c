{
    HAL_Init();
    __HAL_RCC_GPIOD_CLK_ENABLE();
    pca9685_init();

    // 开机强制平滑回位
    homing_all_servos();

    while(1)
    {
        play_song(auto_song, song_length);
        HAL_Delay(3000); 
    }
}