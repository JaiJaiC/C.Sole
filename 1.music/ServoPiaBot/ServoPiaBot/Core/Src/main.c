/* =======================================================================
 * 驱动与控制函数
 * ======================================================================= */
// 加上 static 和强制类型转换，彻底消灭所有 Warning
static void set_servo_angle(uint8_t id, uint8_t angle) {
    uint32_t pulse = 500 + ((uint32_t)angle * 2000 / 180);
    pca9685_set_mk(id, (int)pulse); 
}

static void play_song(NoteEvent* song, uint16_t length) {
    for(int i = 0; i < length; i++) {
        uint8_t current_note = song[i].note_id;
        
        if(current_note == 255) {
            HAL_Delay(song[i].duration + song[i].delay_next);
            continue;
        }

        // 加了 (uint8_t) 强转，消除丢失精度的 Warning
        set_servo_angle(current_note, (uint8_t)servo_press_target[current_note]);
        HAL_Delay(song[i].duration); 
        
        set_servo_angle(current_note, (uint8_t)(90 + servo_offset[current_note]));
        HAL_Delay(song[i].delay_next); 
    }
}

static void homing_all_servos(void) {
    for(int i = 0; i < 64; i++) {
        // 加了 (uint8_t) 强转，消除 Warning
        set_servo_angle((uint8_t)i, (uint8_t)(90 + servo_offset[i])); 
        HAL_Delay(15); 
    }
    HAL_Delay(1000); 
}

